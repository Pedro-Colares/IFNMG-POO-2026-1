package com.gmail.colaresp47.spaceshare;
public class Reserva {
    
    private String usuario;
    private int horas;

    public Reserva() {
        
    }

    public String getUsuario() {
        return usuario;
    }
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public int getHoras() {
        return horas;
    }
    public void setHoras(int horas) {
        this.horas = horas;
    }
   
}
